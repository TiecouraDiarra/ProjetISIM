    public void Table() {
        String[] matieres = {"Identifiants", "Appellations", "Filières", "Niveaux"};
        String[] afficher = new String[5];

        DefaultTableModel model = new DefaultTableModel(null, matieres);
        String sql = "select * from matieres";

        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection(url, user, password);

            Statement st = con.createStatement();
            rs = st.executeQuery(sql);

            while (rs.next()) {
                afficher[0] = rs.getString("idmatiere");
                afficher[1] = rs.getString("appellation");
                afficher[2] = rs.getString("filiere");
                afficher[3] = rs.getString("niveau");
                model.addRow(afficher);
            }
            jTable1.setModel(model);
            con.close();
        } catch (Exception e) {

        }
    }
    private void Rechercher(String recherche){
        String util[] = {
            "Identifiants", 
            "Appellations", 
            "Filières", 
            "Niveaux"
        };
        String vue[] = new String[5];
        DefaultTableModel model = new DefaultTableModel(null, util);
        String sql = "select * from matieres where idmatiere like '%"+recherche+"%'";
        try {
            Connect();
            Statement st = con.createStatement();
            rs = st.executeQuery(sql);
            while (rs.next()) {
                vue[0] = rs.getString("idmatiere");
                vue[1] = rs.getString("appellation");
                vue[2] = rs.getString("filiere");
                vue[3] = rs.getString("niveau");
                model.addRow(vue);
            }
            jTable1.setModel(model);
            con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }


