    private void BaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BaddActionPerformed
        // Ajouter une matière 
        if (txtidentifiant.getText().trim().isEmpty() || txtappellation.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Tous les champs sont obligatoires!");

        } else {
            try {
                Connect();
                String sql = "insert into matieres (idmatiere,appellation,filiere,niveau) value(?,?,?,?)"; //La requete pour faire appel aux imformations dans la base
                pst = con.prepareStatement(sql);
                pst.setString(1, txtidentifiant.getText());
                pst.setString(2, txtappellation.getText());
                pst.setString(3, txtfil.getSelectedItem().toString());
                pst.setString(4, txtniv.getSelectedItem().toString());
                pst.execute(); // EXECUTION DE LA REQUETE

                //con.close();
                JOptionPane.showMessageDialog(null, "Enregistrement réussi");
                Table();
                Actualiser();
                Badd.setEnabled(false);
            } catch (Exception e) {
                //e.printStackTrace(); //POUR GERER LES ERREURS 
                JOptionPane.showMessageDialog(null, "La matière " + txtidentifiant.getText() + " " + "est deja enregistré");
            }
        }
    }//GEN-LAST:event_BaddActionPerformed

