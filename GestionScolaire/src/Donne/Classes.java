     //==================METHODDE POUR AFFICHER LA LISTE DANS UN TABLEAU====================================
     public void Table() {
        String[] classes = {"ID", "CLASSES"};
        String[] afficher = new String[3];

        DefaultTableModel model = new DefaultTableModel(null, classes);
        String sql = "select * from classes";

        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection(url, user, password);

            Statement st = con.createStatement();
            rs = st.executeQuery(sql);

            while (rs.next()) {
                afficher[0] = rs.getString("id");
                afficher[1] = rs.getString("classe");
                model.addRow(afficher);
            }
            jTable1.setModel(model);
            con.close();
        } catch (Exception e) {

        }
    }
     
